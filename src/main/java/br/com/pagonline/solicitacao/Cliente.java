package br.com.pagonline.solicitacao;

public class Cliente {

    private String id;
    private String nome;
    private String url;
    private String status;

    public String getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }

    public String getUrl() {
        return url;
    }

    public String getStatus() {
        return status;
    }
}
